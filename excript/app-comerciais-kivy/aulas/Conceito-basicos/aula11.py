num1 = input("Digite um número: ")
num1 = int(num1)

num2 = input("Digite outro número: ")
num2 = int(num2)

if num1 == num2:
    print(f"O número '{num1}' é igual ao número '{num2}'")

if num1 != num2:
    print(f"O número '{num1}' é diferente do número '{num2}'")

if num1 < num2:
    print(f"O número '{num1}' é menor que o número '{num2}'")

if num1 > num2:
    print(f"O número '{num1}' é maior que o número '{num2}'")

if num1 >= num2:
    print(f"O número '{num1}' é maior ou igual ao número '{num2}'")

if num1 <= num2:
    print(f"O número '{num1}' é menor ou igual ao número '{num2}'")
